score = int(input("Enter Score Grade:"))

if score >= 80:
        print("Your grade is an A+")
elif score >= 70:
        print("Your grade is an A")
elif score >= 60:
        print("Your grade is an A-")
elif score >= 50:
        print("Your grade is an B")
elif score >= 40:
        print("Your grade is an C")
elif score >= 33:
        print("Your grade is an D")
else:
        print ("Sorry, you are failed")